    private void verificarExistenciaImagemPerfil() {
        if (!jTextField1.getText().isEmpty()) {
            try {
                Usuario usr = getControle().getControleConexoes().obterUsuario(jTextField1.getText());

                if (usr.getSHA256ImagemPerfil()!= null) {
                    ItemFilaDownload ifila = new ItemFilaDownload(usr.getSHA256ImagemPerfil(), new InterfaceHook<ArquivoExtendido>(){
                        @Override
                        public void run(ArquivoExtendido obj) {
                            if (obj != null) {
                                setAvatar(obj);
                            }
                        }
                    });

                    getControle().getControleConexoes().novoDownload(ifila);
                }
                
                else {
                    jLabel1.setIcon(Helpers.IMG_ICON_PADRAO);
                }
            } catch (RespostaTimeoutException ex) {
                JOptionPane.showMessageDialog(this, "Falha ao verificar exist√™ncia de usu√°rio.");
            } catch (OfflineException ex) {
                
            } catch (ConexaoEncerrada ex) {
                
            } catch (SemUsuarioException ex) {
                jLabel1.setIcon(Helpers.IMG_ICON_PADRAO);
            }
        }
        
        else {
            jLabel1.setIcon(Helpers.IMG_ICON_PADRAO);
        }
    }
    
    public void setAvatar(ArquivoExtendido arq) {
        try {
            jLabel1.setIcon(Helpers.redmImagemLabel(arq.getArquivo(), jLabel1));
        } catch (IOException ex) {}
    }

