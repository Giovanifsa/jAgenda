    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        idArquivoRecebido = null;
        respostaEnvioRecebido = false;
        
        if (arquivoImagem != null) {      
            ItemFilaUpload item = new ItemFilaUpload(new InterfaceHook<String>() {
                @Override
                public void run(String obj) {
                    JanelaConta.this.respostaEnvioArquivoRecebido(obj);
                }
            }, arquivoImagem);
            
            getControle().getControleConexoes().novoUpload(item);
            
            while (!respostaEnvioRecebido) {
                try {
                    sleep(10);
                } catch (InterruptedException ex) {}
            }
            
            //dialog.dispose();
            
            if (idArquivoRecebido == null) {
                JOptionPane.showMessageDialog(this, "Imagem não alterada devido uma falha.!");
            }
            
            else {
                try {
                    getControle().getControleConexoes().alterarImagemPerfil(usuarioNaoEditado, idArquivoRecebido);
                    
                    usuarioNaoEditado.setSHA256ImagemPerfil(idArquivoRecebido);
                    avatarAnterior = arquivoImagem;

                    JOptionPane.showMessageDialog(this, "Imagem alterada com sucesso!");
                } catch (ConexaoEncerrada | RespostaTimeoutException | SemUsuarioException | ArquivoInexistenteException | OfflineException ex) {
                    JOptionPane.showMessageDialog(this, "Imagem não alterada devido uma falha!");
                }
            }
        }
        
        if (!jTextField1.getText().equals(usuarioNaoEditado.getApelidoUsuario())) {
            try {
                getControle().getControleConexoes().alterarApelido(usuarioNaoEditado, jTextField1.getText());
                
                usuarioNaoEditado.setApelidoUsuario(jTextField1.getText());
            
                JOptionPane.showMessageDialog(this, "Apelido alterado com sucesso!");
            } catch (ConexaoEncerrada | SemUsuarioException | OfflineException | RespostaTimeoutException ex) {
                JOptionPane.showMessageDialog(this, "Apelido não alterado devido uma falha!");
            }
        }
        
        if (jPasswordField1.getPassword().length != 0) {
            try {
                getControle().getControleConexoes().alterarSenha(usuarioNaoEditado, new String(jPasswordField1.getPassword()));
                
                JOptionPane.showMessageDialog(this, "Senha alterada com sucesso!");
            } catch (ConexaoEncerrada | SemUsuarioException | OfflineException | RespostaTimeoutException ex) {
                JOptionPane.showMessageDialog(this, "Senha não alterada devido uma falha!");
            }
        }
        
        jPasswordField1.setText("");
    }//GEN-LAST:event_jButton2ActionPerformed

    private void respostaEnvioArquivoRecebido(String resposta) {
        idArquivoRecebido = resposta;
        respostaEnvioRecebido = true;
    }
    
    private void setAvatarDownloadConcluido(ArquivoExtendido arq) {
        try {
            if (arquivoImagem == null) {
                jLabel11.setIcon(Helpers.redmImagemLabel(arq.getArquivo(), jLabel11));
            }
            
            avatarAnterior = arq;
        } catch (IOException ex) {}
    }
    
