    private void jLabel10MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel10MouseClicked
        JFileChooser jfc = new JFileChooser();
        jfc.setFileFilter(ArquivoExtendido.EXTENSOES_IMAGENS);
        jfc.setMultiSelectionEnabled(false);
        
        if (jfc.showOpenDialog(this) == JFileChooser.APPROVE_OPTION) {
            try {
                arquivoImagem = new ArquivoExtendido(jfc.getSelectedFile());
                
                jLabel11.setIcon(Helpers.redmImagemLabel(arquivoImagem.getArquivo(), jLabel11));
                jLabel9.setVisible(true);
            } catch (IOException ex) {
                JOptionPane.showMessageDialog(this, "Não foi possível carregar o arquivo.");
            } catch (SizeLimitExceededException ex) {
                JOptionPane.showMessageDialog(this, ex.getMessage());
            }
        }
    }//GEN-LAST:event_jLabel10MouseClicked

