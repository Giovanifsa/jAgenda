    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        idArquivoRecebido = null;
        respostaEnvioRecebido = false;
        
        boolean erroObtido = false;
        janelaCarregamento = new JanelaLoading(new ImageIcon(this.getClass().getResource("/visao/imagens/64_64/loading_wifi.gif")), "Aguarde", "Enviando imagem de perfil...", null);
        
        if (arquivoImagem != null) {          
            ItemFilaUpload item = new ItemFilaUpload(new InterfaceHook<String>() {
                @Override
                public void run(String obj) {
                    JanelaNovaConta.this.respostaEnvioArquivoRecebido(obj);
                }
            }, arquivoImagem);
            
            getControle().getControleConexoes().novoUpload(item);
            
            janelaCarregamento.setVisible(true);
            
            while (!respostaEnvioRecebido) {
                try {
                    sleep(10);
                } catch (InterruptedException ex) {}
            }
            
            //dialog.dispose();
            
            if (idArquivoRecebido == null) {
                janelaCarregamento.setVisible(false);
                
                JOptionPane.showMessageDialog(this, "Erro ao criar conta: Falha ao enviar a imagem.");
                
                erroObtido = true;
            }
        }
        
        if (!erroObtido) {
            try {
                janelaCarregamento.alterarTexto("Quase lá", "Terminando de criar sua conta...", null);
                janelaCarregamento.alterarIcone(new ImageIcon(this.getClass().getResource("/visao/imagens/64_64/loading_sol.gif")));
                janelaCarregamento.setVisible(true); 
                
                getControle().getControleConexoes().criarConta(jTextField1.getText(), new String(jPasswordField1.getPassword()), jTextField2.getText(), jTextField3.getText(), idArquivoRecebido);
                
                janelaCarregamento.dispose();
                
                JOptionPane.showMessageDialog(this, "Cadastro concluído!");
                
                getControle().getControleJanelas().fecharJanelaNovaConta();
            } catch (OfflineException ex) {
                
            } catch (RespostaTimeoutException ex) {
                JOptionPane.showMessageDialog(this, ex.getMessage());
            } catch (NovaContaException ex) {
                JOptionPane.showMessageDialog(this, ex.getMessage());
            } catch (IOException ex) {
                Logger.getLogger(JanelaNovaConta.class.getName()).log(Level.SEVERE, null, ex);
            } catch (ConexaoEncerrada ex) {
                Logger.getLogger(JanelaNovaConta.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

